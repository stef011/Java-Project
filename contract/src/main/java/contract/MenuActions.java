package contract;

/**
 * <h1>The MenuActions Enumeration</h1>
 * Used to perform a menu Action
 * @author Exars 18-23 Strasbourg grp1
 * @version 1.0
 */
public enum MenuActions {
	/** Resumes the game */
	Resume,
	/** Quits the game */
	Quitgame,
	/** Play the game */
	Play,
	/** No action */
	Nop, 
	/** Goes to the main menu */
	Mainmenu
}
